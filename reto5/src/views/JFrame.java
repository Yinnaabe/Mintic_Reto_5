    public void llenarLista() {
        List<RolModel> lista = rolController.obtenerRoles();
        String[] arreglo = new String[lista.size()];

        int i = 0;
        for (RolModel rolModel : lista) {
            arreglo[i] = rolModel.toString();
            i++;
        }

        jList3.setListData(arreglo);
    }

